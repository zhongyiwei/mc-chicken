            <h2 class="title">Why Identify & Establish Values?</h2>
            <div class="entry">
<br/>
Your values are made up of everything that has happened to you in your life and include influences from: your parents and family, your religious affiliation, your friends and peers, your education, your reading, and more. Effective people recognize these environmental influences and identify and develop a clear, concise, and meaningful set of values/beliefs, and priorities. Once defined, values impact every aspect of your life.<br/><br/>
<p>You demonstrate and model your values in action in your personal and work behaviors, decision making, contribution, and interpersonal interaction.</p>
<li>You use your values to make decisions about priorities in your daily work and home life.</li>
<li>Your goals and life purpose are grounded in your values.</li>

